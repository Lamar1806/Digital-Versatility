    <script>
        document.addEventListener("DOMContentLoaded", function(event){
            changeIcon = function(){
                var target, links, nav = '';
                target = document.getElementById('js-mobile'); //mobile button
                nav = document.getElementById('js-nav-sticky'); //navbar container
                links = document.getElementById('js-links');
                
                if(target.classList.contains('ion-android-options')){
                    target.classList.remove('ion-android-options');
                    target.classList.add('ion-close-round');
                    links.style.display = 'inline-block';
                    nav.style.height = 'auto';
                    
                }else{
                    target.classList.remove('ion-close-round');
                    target.classList.add('ion-android-options');
                    links.style.display = 'none';
                    nav.style.height = '60px';
                }                              
            }
            //remove dropdown when the page is scrolled
            removeDropDown = function(){
                var target, links, nav = '';
                target = document.getElementById('js-mobile');
                nav = document.getElementById('js-nav-sticky');
                links = document.getElementById('js-links');

                if(target.classList.contains('ion-close-round')){
                    target.classList.remove('ion-close-round');
                    target.classList.add('ion-android-options');
                    links.style.display = 'none';
                    nav.style.height = '60px';
                }
            }
            //if window is greater than 700 regular nav appears.
            normalNav = function(){
                var target, links, nav = '';
                nav = document.getElementById('js-nav-sticky');
                links = document.getElementById('js-links');
                target = document.getElementById('js-mobile');

                if(window.screen.width > 700){
                    links.style.display = "inline-block";
                    target.style.display = "none";
                }else{
                    links.style.display = "none";
                    target.style.display = "inline-block";
                }              
            }
            document.getElementById('js-mobile').addEventListener('click', changeIcon);
            window.addEventListener('scroll', removeDropDown);
            window.addEventListener('resize', normalNav);
        })
    </script> 
